public class doubleMath{
	public static double dmPlus(double rhs, double lhs) {
		return rhs + lhs;
		}
	public static double dmMin(double rhs, double lhs) {
		return lhs - rhs;
		}
	public static double dmMul(double rhs, double lhs) {
		return rhs * lhs;
		}
	public static double dmDiv(double rhs, double lhs) {
		return lhs / rhs;
		}
	public static double dmMod(double rhs, double lhs) {
		return lhs % rhs;
		}
	}
